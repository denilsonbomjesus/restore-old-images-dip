import cv2
import numpy as np
import matplotlib.pyplot as plt

# === PARÂMETROS AJUSTÁVEIS ===
kernel_size = 3     # Deve ser ímpar >= 3
alpha = 0.7         # Entre 0.0 (sem suavizar) e 1.0 (suavização total)

# ==============================

# Carrega imagem
img_original = cv2.imread('old_photo_09.jpeg')
img_corrigida_salt_pepper = img_original.copy()

# Lista de recortes (x1, x2, y1, y2)
areas_para_corrigir = [
    (140, 258, 175, 258),
    (20, 100, 200, 258),
    (0, 60, 0, 100),
    (150, 200, 0, 75),
]

# Aplicar filtro de mediana com mistura nas áreas definidas
for x1, x2, y1, y2 in areas_para_corrigir:
    recorte = img_corrigida_salt_pepper[y1:y2, x1:x2]
    recorte_median = cv2.medianBlur(recorte, kernel_size)
    recorte_denoised = cv2.addWeighted(recorte, 1 - alpha, recorte_median, alpha, 0)
    img_corrigida_salt_pepper[y1:y2, x1:x2] = recorte_denoised

# === SEGUNDA ETAPA: INPAINTING EM REGIÃO ESPECÍFICA ===

# Definir área do rasgo
x1, x2 = 150, 200
y1, y2 = 50, 200
recorte_rasgo = img_corrigida_salt_pepper[y1:y2, x1:x2]

# Equalização de contraste e criação de máscara
gray_rasgo = cv2.cvtColor(recorte_rasgo, cv2.COLOR_BGR2GRAY)
equalizado = cv2.equalizeHist(gray_rasgo)
_, mask = cv2.threshold(equalizado, 220, 255, cv2.THRESH_BINARY)

# Operação morfológica para suavizar a máscara
kernel = cv2.getStructuringElement(cv2.MORPH_RECT, (3, 7))
mask = cv2.morphologyEx(mask, cv2.MORPH_CLOSE, kernel, iterations=1)

# Aplicação de inpainting
recorte_corrigido = cv2.inpaint(recorte_rasgo, mask, 3, cv2.INPAINT_TELEA)

# Substituir área na imagem final
img_final = img_corrigida_salt_pepper.copy()
img_final[y1:y2, x1:x2] = recorte_corrigido

# === EXIBIÇÃO DOS RESULTADOS ===
plt.figure(figsize=(18, 6))

plt.subplot(1, 3, 1)
plt.imshow(cv2.cvtColor(img_original, cv2.COLOR_BGR2RGB))
plt.title('Imagem Original')
plt.axis('off')

plt.subplot(1, 3, 2)
plt.imshow(cv2.cvtColor(img_corrigida_salt_pepper, cv2.COLOR_BGR2RGB))
plt.title(f'Após Suavização (α={alpha})')
plt.axis('off')

plt.subplot(1, 3, 3)
plt.imshow(cv2.cvtColor(img_final, cv2.COLOR_BGR2RGB))
plt.title('Final com Inpainting')
plt.axis('off')

plt.tight_layout()
plt.show()

cv2.imwrite('old_photo_09_corrigida.jpeg', img_final)

